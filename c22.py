#program using break statment
for i in [1,2,3,4,5,6]:
    if (i==3):
        break
    print(i)
#program using continue statment
for i in [1,2,3,4,5,6]:
    if (i==3):
        continue
    print(i)
#program of range statement using for loop
for i in range(20):
    if(i%2==0):
      continue
    print(i)
